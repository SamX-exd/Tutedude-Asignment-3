"""
Using the Math Module for Calculations
"""

import math

nums = int(input("Enter a number:"))

square_root = math.sqrt(nums)
logarithm = math.log(nums)
sine = math.sin(nums)

print(f"Square root of {nums} is : {square_root}")
print(f"Logarithm of {nums} is : {logarithm}")
print(f"Sine of {nums} is : {sine}")